# datatypes in python

# int # non decimal

num1 = 1034343434434
print(num1)
print(type(num1))

# float # decimal

num2 = 10.123456789
print(num2)
print(type(num2))

# complex

num3 = 1 + 2j
print(num3)
print(type(num3))

# str

str1 = "bascom bridge"
print(str1)
print(type(str1))

# bool

isLogin = False
print(isLogin)
print(type(isLogin))

